        $error = 'All fields are required.';
    } else {
        try {
            $dsn = "mysql:host={$dbHost};port={$dbPort};dbname={$dbName};charset=utf8mb4";
            $pdo = new PDO($dsn, $dbUser, $dbPass, [
                PDO::ATTR_ERRMODE => PDO::ERRMODE_EXCEPTION,
                PDO::ATTR_DEFAULT_FETCH_MODE => PDO::FETCH_ASSOC,
            ]);

            // Run schema (DDL only ã stop before seed data)
            $sql = file_get_contents($schemaFile);
            $statements = array_filter(array_map('trim', explode(';', $sql)));
            foreach ($statements as $stmt) {
                if ($stmt) {
                    $pdo->exec($stmt);
                }
            }

            // Seed default data only if tables are empty (prevents duplicates on re-install)
            $deptCount = (int)$pdo->query('SELECT COUNT(*) FROM departments')->fetchColumn();
            if ($deptCount === 0) {
                $pdo->exec("INSERT IGNORE INTO `departments` (`name`, `color`, `sort_order`) VALUES
                    ('UCC+ 9', '#7c3aed', 1),
                    ('UCC Broadband', '#2563eb', 2),
                    ('UCC Hosted', '#059669', 3),
                    ('UCC Leased Line', '#d97706', 4),
                    ('UCC Sales', '#dc2626', 5)");
            }

            $cannedCount = (int)$pdo->query('SELECT COUNT(*) FROM canned_responses')->fetchColumn();
            if ($cannedCount === 0) {
                $pdo->exec("INSERT IGNORE INTO `canned_responses` (`shortcut`, `title`, `content`) VALUES
                    ('hello', 'Greeting', 'Hello! Thank you for contacting us. How can I help you today?'),
                    ('thanks', 'Thank You', 'Thank you for getting in touch! Is there anything else I can help you with?'),
                    ('wait', 'Please Wait', 'Please give me a moment while I look into this for you.'),
                    ('bye', 'Goodbye', 'Thank you for chatting with us today. Have a great day!'),
                    ('ticket', 'Ticket Created', 'I''ve raised a support ticket for you. Our team will follow up via email shortly.')");
            }

            $triggerCount = (int)$pdo->query('SELECT COUNT(*) FROM triggers')->fetchColumn();
            if ($triggerCount === 0) {
                $pdo->exec("INSERT INTO `triggers` (`name`, `type`, `condition_value`, `message`, `is_active`) VALUES
                    ('Welcome Message', 'time', '5', 'Hi there! Þ Is there anything we can help you with today?', 1),
                    ('Offline Reply', 'offline', NULL, 'Thanks for your message! We''re currently offline but will get back to you as soon as possible. Please leave your email so we can follow up.', 1)");
            }

            // Create admin account
            $hash = password_hash($adminPass, PASSWORD_BCRYPT);
            $stmt = $pdo->prepare("INSERT INTO agents (name, email, password_hash, role, status) VALUES (?, ?, ?, 'admin', 'online') ON DUPLICATE KEY UPDATE role='admin'");
            $stmt->execute([$adminName, $adminEmail, $hash]);

            // Write config.php
            $configContent = <<<PHP
<?php
// ============================================================
// Chat System Configuration
// Generated by installer ã edit as needed
// ============================================================

define('DB_HOST', '{$dbHost}');
define('DB_PORT', '{$dbPort}');
define('DB_NAME', '{$dbName}');
define('DB_USER', '{$dbUser}');
define('DB_PASS', '{$dbPass}');

// Full URL to this chat/ directory (no trailing slash)
define('APP_URL', '{$appUrl}');

// Secret key for JWT signing ã keep private!
define('JWT_SECRET', '{$jwtSecret}');

// JWT token lifetime in seconds (default 8 hours)
define('JWT_TTL', 28800);

// WhatsApp Cloud API ã fill in from Meta Developer Portal
define('WA_PHONE_NUMBER_ID', '');
define('WA_ACCESS_TOKEN', '');
define('WA_VERIFY_TOKEN', '');
define('WA_API_VERSION', 'v18.0');

// CORS: comma-separated allowed origins, or '*' for any
// e.g. 'https://yourwebsite.com,https://www.yourwebsite.com'
define('ALLOWED_ORIGINS', '*');

// Uploads directory (relative to chat/ root)
define('UPLOAD_DIR', __DIR__ . '/uploads/');
define('UPLOAD_URL', APP_URL . '/uploads/');
define('MAX_UPLOAD_MB', 10);
PHP;

            file_put_contents($configFile, $configContent);

            $success = 'Installation complete!';
            $step = 3;
        } catch (Exception $e) {
            $error = 'Installation failed: ' . htmlspecialchars($e->getMessage());
        }
    }
}

